{
    /*Дано трехзначное число. Выяснить, является ли оно палиндромом ("перевертышем"), 
    т. е. таким числом, десятичная запись которого читается одинаково
    слева направо и справа налево.*/
    let n, d1, d2, d3;
    n=parseInt(prompt("введите ваше число"));
    d1 = n % 10; //единицы
    d2 = Math.floor(n / 10 ) % 10; // десятки
    d3 = Math.floor(n / 100 ) % 10; // сотни
    
    if (d1 === d3) {
        alert("Палиндром");
    } else {
        alert("Не_палиндром");
    }
}
