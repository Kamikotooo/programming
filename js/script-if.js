{
    /*. Найти:
    а) сумму кубов всех целых чисел от 20 до 40;
    б) сумму квадратов всех целых чисел от a до 50 (значение a вводится
    с клавиатуры; 0 a 50);
    в) сумму квадратов всех целых чисел от 1 до n (значение n вводится
    с клавиатуры; 1 n 100);
    г) сумму квадратов всех целых чисел от a до b (значения a и b вводятся с клавиатуры; b a).*/

    
    // а) Сумма кубов чисел от 20 до 40
    let sumCubes = 0, i;
    for (i = 20; i <= 40; i++) {
        sumCubes += i ** 3;
    }
    alert("Сумма кубов чисел от 20 до 40: " +sumCubes);

    // б) Сумма квадратов чисел от a до 50
    let sumSquare = 0, a;
    a = parseInt(prompt("Введите число a:"));

    for (let i = a; i <= 50; i++) {
        sumSquare += i ** 2;
    }
    alert("Сумма квадратов чисел от A до 50: " +sumSquare);

    // в) Сумма квадратов чисел от 1 до n
    let sumSquaresN = 0, n;
    n = parseInt(prompt("Введите число n:"));
    
    for (let i = 1; i <= n; i++) {
        sumSquaresN += i ** 2;
    }
    alert("Сумма квадратов чисел от 1 до n: " +sumSquaresN);

    // г) Сумма квадратов чисел от a до b
    let sumSquaresAtoB = 0, a2, b;
    a2 = parseInt(prompt("Введите число a:"));
    b = parseInt(prompt("Введите число b:"));
 
    for (let i = a2; i <= b; i++) {
        sumSquaresAtoB += i ** 2;
    }
    alert("Сумма квадратов чисел от a до b: " +sumSquaresAtoB);
}
