{
    /*Дана непустая последовательность целых чисел, оканчивающаяся нулем.
    Найти:
    а) сумму всех чисел последовательности, больших числа x;
    б) количество всех четных чисел последовательности.*/
    // Ввод числа x
    let x = parseInt(prompt("Введите число x:"));
    let sumG = 0; // Сумма чисел, больших x
    let count = 0; // Количество четных чисел
    let number;
    while (true) {
        number = parseInt(prompt("Введите число (0 для завершения):"));
        if (number === 0) {
            break;
        }

        if (number > x) {
            sumG += number; // Добавляем к сумме, если больше x
        }
        if (number % 2 === 0) {
            count++; // Увеличиваем счетчик, если четное
        }
    }

    alert("Сумма всех чисел, больших x: " +sumG);
    alert("Количество четных чисел: " +count);

}



